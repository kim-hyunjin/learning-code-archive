package com.eomcs.basic.ex03;

// 리터럴(literal)
// - 자바 언어에서 값을 표기하는 문법

public class Exam21 {
  public static void main(String[] args) {
    // 10진수 리터럴
    System.out.println(100);
    
    // 8진수 리터럴
    System.out.println(0144);   //0숫자

    // 2진수
    System.out.println(0b1100100);  //0b숫자
    System.out.println(0B1100100);
    // 16진수
    System.out.println(0x64);   //0x숫자
    System.out.println(0X64);
    System.out.println(0X064);
    System.out.println(0X0064);
  }
}